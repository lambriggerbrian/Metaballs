using UnityEngine;
using System.Collections.Generic;

public class MarchingGrid
{
    private BlobContainer container;

    // Grid dimensions in cubes
    public int XCubes, YCubes, ZCubes;

    // Offset maps
    private Vector3[] vertMap;
    private Vector3[,,] cubeMap;

    // Data for metaballs
    public GridCube[] Cubes { get; private set; }
    public List<int> Triangles { get; private set; }
    public List<Vector3> Vertices { get; private set; }

    // Verts and lines for grid debug drawing
    public Vector3[,,] GridVerts { get; private set; }
    public List<Vector3> GridLines;

    // Compute shader properties and buffers
    private ComputeShader shader;
    private int shaderKernel;
    private GPUBuffers gpuBuffers;

    private bool initialized;

    public MarchingGrid(BlobContainer container, ComputeShader shader)
    {
        this.container = container;
        this.shader = shader;

        // Get Num of Cubes in each axis by dividing by resolution 
        XCubes = Mathf.RoundToInt(container.Size.x / container.Resolution);
        YCubes = Mathf.RoundToInt(container.Size.y / container.Resolution);
        ZCubes = Mathf.RoundToInt(container.Size.z / container.Resolution);

        GridLines = new List<Vector3>();
        Vertices = new List<Vector3>();
        initialize();
    }

    // ============================================================================================
    // Public Functions ---------------------------------------------------------------------------
    // ============================================================================================

    public void EvaluateAll(Blob[] blobs)
    {
        // Initialize and clear vertices
        if (!initialized) initialize();
        Vertices.Clear();

        // Convert Blobs to GPUBlobs
        GPUBlob[] gpuBlobs = new GPUBlob[blobs.Length];
        for (int i = 0; i < blobs.Length; i++)
        {
            Blob blob = blobs[i];
            gpuBlobs[i].Pos = blob.transform.localPosition;
            gpuBlobs[i].Factor = blob.Factor;
        }

        // Compute shader time
        GPUVertices[] vertBuffer = runComputeShader(gpuBlobs);

        // Update vertices for marching cubes algorithm
        for (int x = 0; x < XCubes; x++)
        {
            for (int y = 0; y < YCubes; y++)
            {
                for (int z = 0; z < ZCubes; z++)
                {
                    updateVertices(vertBuffer[x + XCubes * (y + YCubes * z)]);
                }
            }
        }
    }

    public void Destroy()
    {
        gpuBuffers.CubeBuffer.Release();
        if (gpuBuffers.ValuesBuffer != null) gpuBuffers.ValuesBuffer.Release();
        gpuBuffers.BlobBuffer.Release();
        gpuBuffers.VertMapBuffer.Release();
        gpuBuffers.VertBuffer.Release();
        Triangles = null;
    }

    // ============================================================================================
    // Metaball Algorithm Functions ---------------------------------------------------------------
    // ============================================================================================

    private GPUVertices[] runComputeShader(GPUBlob[] gpuBlobs)
    {
        // Pass Data
        gpuBuffers.BlobBuffer.SetData(gpuBlobs);
        shader.SetInt("numBlobs", gpuBlobs.Length);
        shader.SetInt("xCubes", XCubes);
        shader.SetInt("yCubes", YCubes);
        shader.SetFloat("threshold", container.Threshold);

        // Why waste time with lot instruction to lot data when few instruction do trick
        shader.Dispatch(shaderKernel, XCubes / 8, YCubes / 8, ZCubes / 8);

        GPUVertices[] output = new GPUVertices[gpuBuffers.VertBuffer.count];
        gpuBuffers.VertBuffer.GetData(output);
        return output;
    }

    private void updateVertices(GPUVertices vert)
    {
        int cubeIndex = vert.Index;

        for (int k = 0; triTable[cubeIndex][k] != -1; k += 3)
        {
            Vertices.Add(GridUtils.FindVertex(vert, triTable[cubeIndex][k]));
            Vertices.Add(GridUtils.FindVertex(vert, triTable[cubeIndex][k + 2]));
            Vertices.Add(GridUtils.FindVertex(vert, triTable[cubeIndex][k + 1]));
        }
    }

    // ============================================================================================
    // Setup Functions ---------------------------------------------------------------------------
    // ============================================================================================

    private void initialize()
    {
        instantiateVertMap();
        instantiatePositionMap();
        instantiateGridCubes();
        instantiateGridLines();
        instantiateComputeShader();
        initialized = true;
    }

    // Get offset of each vert
    private void instantiateVertMap()
    {
        vertMap = new Vector3[] {
            new Vector3(-1, -1, -1), // v0: Bottom-Left Near
            new Vector3(1, -1, -1),  // v1: Bottom-Right Near
            new Vector3(1, 1, -1),   // v2: Top-Right Near
            new Vector3(-1, 1, -1),  // v3: Top-Left Near
            new Vector3(-1, -1, 1),  // v4: Bottom-Left Far
            new Vector3(1, -1, 1),   // v5: Bottom-Right Far
            new Vector3(1, 1, 1),    // v6: Top-Right Far
            new Vector3(-1, 1, 1)    // v7: Top-Left Far
        };

        // Scale vertex map
        for (int i=0; i<8; ++i)
        {
            // Vertices are offsets from center of grid cube
            vertMap[i] /= 2;
            vertMap[i] = new Vector3(vertMap[i].x / XCubes,
                vertMap[i].y / YCubes,
                vertMap[i].z / ZCubes);
        }
    }

    // Get unscaled offset of each grid cube
    private void instantiatePositionMap()
    {
        cubeMap = new Vector3[XCubes, YCubes, ZCubes];

        for (int x = 0; x < XCubes; x++)
        {
            for (int y = 0; y < YCubes; y++)
            {
                for (int z = 0; z < ZCubes; z++)
                {

                    float xCoord = ((float)x / XCubes) - 0.5f;
                    float yCoord = ((float)y / YCubes) - 0.5f;
                    float zCoord = ((float)z / ZCubes) - 0.5f;

                    cubeMap[x, y, z] = new Vector3(xCoord, yCoord, zCoord);
                }
            }
        }
    }

    // Grid cube to center and verts in buffer for GPU
    private void instantiateGridCubes()
    {
        GridVerts = new Vector3[XCubes + 1, YCubes + 1, ZCubes + 1];
        Cubes = new GridCube[XCubes * YCubes * ZCubes];

        for (int x = 0; x < XCubes; x++)
        {
            for (int y = 0; y < YCubes; y++)
            {
                for (int z = 0; z < ZCubes; z++)
                {
                    Vector3 centerPoint = cubeMap[x, y, z];
                    Cubes[x + XCubes * (y + YCubes * z)].CenterPos = centerPoint;

                    Cubes[x + XCubes * (y + YCubes * z)].V0 = centerPoint + vertMap[0];
                    Cubes[x + XCubes * (y + YCubes * z)].V1 = centerPoint + vertMap[1];
                    Cubes[x + XCubes * (y + YCubes * z)].V2 = centerPoint + vertMap[2];
                    Cubes[x + XCubes * (y + YCubes * z)].V3 = centerPoint + vertMap[3];
                    Cubes[x + XCubes * (y + YCubes * z)].V4 = centerPoint + vertMap[4];
                    Cubes[x + XCubes * (y + YCubes * z)].V5 = centerPoint + vertMap[5];
                    Cubes[x + XCubes * (y + YCubes * z)].V6 = centerPoint + vertMap[6];
                    Cubes[x + XCubes * (y + YCubes * z)].V7 = centerPoint + vertMap[7];

                    GridVerts[x, y, z] = centerPoint + vertMap[0];
                    GridVerts[x+1, y, z] = centerPoint + vertMap[1];
                    GridVerts[x+1, y+1, z] = centerPoint + vertMap[2];
                    GridVerts[x, y+1, z] = centerPoint + vertMap[3];
                    GridVerts[x, y, z+1] = centerPoint + vertMap[4];
                    GridVerts[x+1, y, z+1] = centerPoint + vertMap[5];
                    GridVerts[x+1, y+1, z+1] = centerPoint + vertMap[6];
                    GridVerts[x, y+1, z+1] = centerPoint + vertMap[7];
                }
            }
        }
    }

    private void instantiateGridLines()
    {
        int XLength = GridVerts.GetLength(0);
        int YLength = GridVerts.GetLength(1);
        int ZLength = GridVerts.GetLength(2);

        // Get set of 2D grids first, then connect planes
        for (int x = 0; x < XLength; x++)
        {
            for (int y = 0; y < YLength; y++)
            {
                // Front to Back Line
                GridLines.Add(GridVerts[x, y, 0]);
                GridLines.Add(GridVerts[x, y, ZLength-1]);

                for (int z = 0; z < ZLength; z++)
                {
                    // Left to Right Line
                    GridLines.Add(GridVerts[0, y, z]);
                    GridLines.Add(GridVerts[XLength-1, y, z]);

                    if (y == 0)
                    {
                        // Bottom to Top Line
                        GridLines.Add(GridVerts[x, 0, z]);
                        GridLines.Add(GridVerts[x, YLength-1, z]);
                    }
                }
            }
        }
    }

    // Setup compute buffers and assign to compute shader
    private void instantiateComputeShader()
    {
        // 1 Vec3 = 3 Floats = 12 bytes (3 * 4 bytes each)
        // 1 int = 4 bytes
        
        // Length positions is total number of cubes in grid, flattened into 1D buffer
        // 9 Vec3 per cube, 9 * 12 bytes = 108 bytes per cube
        gpuBuffers.CubeBuffer = new ComputeBuffer(Cubes.Length, 108);
        gpuBuffers.CubeBuffer.SetData(Cubes);

        // 8 verts, 1 Vec3 = 12 bytes per vert
        gpuBuffers.VertMapBuffer = new ComputeBuffer(8, 12);
        gpuBuffers.VertMapBuffer.SetData(vertMap);

        // Length positions, (1 int = 4 bytes) + (12 Vec3 = 144 bytes) = 148 bytes
        gpuBuffers.VertBuffer = new ComputeBuffer(Cubes.Length, 148);
        
        // Length positions, 1 float (4 bytes) + 1 Vec3 (12 bytes) = 16 bytes
        gpuBuffers.BlobBuffer = new ComputeBuffer(Cubes.Length, 16);

        // Get compute shader and assign buffers
        shaderKernel = shader.FindKernel("Calculate");
        shader.SetBuffer(shaderKernel, "cubes", gpuBuffers.CubeBuffer);
        shader.SetBuffer(shaderKernel, "blobs", gpuBuffers.BlobBuffer);
        shader.SetBuffer(shaderKernel, "vertMap", gpuBuffers.VertMapBuffer);
        shader.SetBuffer(shaderKernel, "vertices", gpuBuffers.VertBuffer);
    }


